/// Фазы луны
public enum MoonPhase: String {
    case newMoon            = "Новолуние"
    case waxingCrescent     = "1-ая Фаза"
    case firstQuarter       = "1-ая Четверть"
    case waxingGibbous      = "2-ая Фаза"
    case fullMoon           = "Полнолуние"
    case waningGibbous      = "3-яя Фаза"
    case lastQuarter        = "Последняя Четверть"
    case waningCrescent     = "4-ая Фаза"
}
